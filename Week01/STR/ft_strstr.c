#include "../libft.h"

char	*ft_strstr(const char *s1, const char *s2)
{
	int i;
	int n;

	if (s2[0])
	{
		i = 0;
		while (s1[i])
		{
			n = 0;
			while (s1[i + n] == s2[n])
			{
				if (s2[n + 1] == '\0')
					return ((char *)s1 + i);
				n++;
			}
			i++;
		}
		return (NULL);
	}
	return ((char*)s1);
}
